package com.tns.corejava;

public class StaticVariable3 {
	static int p, q, r;
	public static void main(String[] args) {
	System.out.println("p : " +p);
	System.out.println("q : " + q);
	System.out.println("r : " + r);
	p = 10;
	q = 20;
	r = 30;
	System.out.println("p : " +p);
	System.out.println("q : " + q);
	System.out.println("r : " + r);
	}

}
